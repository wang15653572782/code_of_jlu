;8255接口初始化，由CS连接的IOY端口决定。
A8255_A EQU 0600H
A8255_B EQU 0602H
A8255_C EQU 0604H
A8255_CON EQU 0606H

;数码管的数据表，分别表示0-9
DATA SEGMENT
	NUMTAB DB 3FH,06H,5BH,4FH,66H,6DH,7DH,07H,7FH,6FH
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA
START:
	MOV AX,DATA
	MOV DS,AX  ;指定段起始地址
    
	LEA SI,NUMTAB;源变址寄存器
	MOV DX,A8255_CON
	MOV AL,81H  ;81H=1 00 0 0 0 0 1B，A组方式0，A口输出，C口高四位输出；B组方式0，B口输出，C口低四位输入
	OUT DX,AL
    
	MOV CX,0AH		;数字循环10次
	MOV BX,0000H	;位码偏移数
    
DISP: 
	PUSH CX
	MOV CX,06H		;走马灯循环6次
	MOV AL,11011111B
DISP1:
	MOV DX,A8255_A
	OUT DX,AL		;位码输出
	ROR AL,1		;循环右移一位，即灯左移一位
	PUSH AX
	
	MOV AL,[SI+BX]	;将第BX个字符编码放入AL
	MOV DX,A8255_B
	OUT DX,AL		;输出字符
	
	POP AX
	CALL DELAY
	LOOP DISP1
	
	POP CX
	INC BX			;位码偏移数加一
	LOOP DISP
    
	JMP START
    
    
DELAY:
	PUSH CX
	MOV CX,0FFFFH
D1:
	LOOP D1

	MOV CX,0FFFH
	
D2:
	LOOP D2
	
	POP CX
	RET

CODE ENDS
     END START