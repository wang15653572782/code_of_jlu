CODE SEGMENT
	ASSUME CS:CODE
START:
;	CALL JUCHIBO
;	CALL FANGBO
	CALL SANGJIAOBO
DELAY PROC                 
	PUSH CX
	PUSH BX
	MOV CX,0FFH
LL5: MOV BX,0FFH
LL1:	DEC BX
	JNZ LL1
	LOOP LL5
	POP BX
	POP CX
	RET
DELAY ENDP

DELAY1 PROC
	PUSH CX
	PUSH BX
	MOV CX,0FH
LLL1:MOV BX,0FH
LLL2:DEC BX
	JNZ LLL2
	LOOP LLL1
	POP BX
	POP CX
	RET
DELAY1 ENDP
JUCHIBO PROC
	PUSH AX
	PUSH CX
	PUSH DX
Y1: 	MOV AL,00H           ;锯齿波
	MOV DX,0600H
	OUT DX,AL
Y2: INC AL
	MOV DX,0600H
	OUT DX,AL
	MOV CX,0FFH
Y3: LOOP Y3
	JMP Y2
	POP DX
	POP CX
	POP AX
JUCHIBO ENDP
FANGBO PROC
	PUSH AX
	PUSH DX
FF1:    MOV AL,00H                ;方波
	MOV DX,0600H
	OUT DX,AL
	CALL DELAY
	MOV AL,0FFH
	MOV DX,0600H
	OUT DX,AL
	CALL DELAY
	JMP FF1
	POP DX
	POP AX
FANGBO ENDP
SANGJIAOBO PROC
	PUSH AX
	PUSH DX
Z1:     MOV AL,00H             ;三角波
	MOV DX,0600H
Z2:	OUT DX,AL
	CALL DELAY1
	INC AL
	JNZ Z2
	DEC AL              ;两个DEC指令防止出现平顶现象
	DEC AL
Z3: 	OUT DX,AL
	CALL DELAY1
	DEC AL
	JNZ Z3
	JMP Z2
	POP DX
	POP AX
SANGJIAOBO ENDP
CODE ENDS
	END START